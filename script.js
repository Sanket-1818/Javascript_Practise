//Primitive Data
//Number

var a=100
console.log(a)
console.log(typeof(a))

//string

var b='Hi'
console.log(b)
console.log(typeof(b))

//boolean

var c=true
console.log(c)
console.log(typeof(c))


//Non Primitive Data
//undefined

var e
console.log(e)
console.log(typeof(e))

//null

var f=null
console.log(f)
console.log(typeof(f))

//array
var s=[20,40,50]
console.log(s)
console.log(typeof(s))
